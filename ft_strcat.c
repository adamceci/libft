char				*ft_strcat(char *s1, const char *s2)
{
	unsigned int	i;
	char			*str;

	str = s1;
	while (*str)
		str++;
	i = 0;
	while (s2[i])
	{
		str[i] = s2[i];
		i++;
	}
	str[i] = '\0';
	return (s1);
}
